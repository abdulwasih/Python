#Circle Area Program

#importing math library
import math

#inputting radius of the circle
#type conversion from string to float 
radius = float(input("Enter the radius of the circle:\t"))

#area of circle= 4*pi*r*r
area = (math.pi)*(radius**2)  #radius**2  = radius*radius

#printing area of the circle
print("Area of the circle is: ",area)



